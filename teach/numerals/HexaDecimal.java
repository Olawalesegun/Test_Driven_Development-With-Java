package numerals;

public class HexaDecimal {
    private final static int hexNum1 = 0x64;
    private final static int hexNum2 = 0x99;
    private final static int hexNum3 = 0x899;

    public static int getHexNum1() {
        return hexNum1;
    }
    public static int getHexNum2() {
        return hexNum2;
    }
    public static int getHexNum3() {
        return hexNum3;
    }
    public static void print(int num){
        System.out.println(num);
    }
}
